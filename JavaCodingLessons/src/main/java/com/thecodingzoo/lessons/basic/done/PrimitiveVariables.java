package com.thecodingzoo.lessons.basic.done;

public class PrimitiveVariables {

	public static void main(String[] args) {
		// DataTypes and variables
		// CamelCase
		// Different primitive times are
		// below. They are not objects and do not have methods.
		// String is not a primitive but an object.
		// Go over block scope in evaluations or in object programming and final
		// Immutable

		byte byteVariable = 1; // -128 to 128
		short shortVariable = -1; // -32768 to 32767
		int integerVariable = 1; // -2,147,483,648 to 2,147,483,647
		long longVariable = 1; // -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807

		float floatVariable = 1.23f; // 32 bits - single precision
		double doubleVariable = 1.23; // 64 bits - double precision

		System.out.println(byteVariable);
		System.out.println(shortVariable);
		System.out.println(integerVariable);
		System.out.println(longVariable);
		System.out.println(floatVariable);
		System.out.println(doubleVariable);

		char charVariable = 'a'; // Unicode character set that can rrepresent all international characters.
		boolean booleanVariable = false;
		String myName = "shane";
	}

}
